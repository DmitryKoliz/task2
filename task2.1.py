#Выведение третьего символа строки
str1 = 'Hello world'
print(str1[2])

#Выведение предпоследнего символа
str2 = 'Hello world'
print(str2[-2])

#Выведение первых пяти символов строки
str3 = 'Hello world'
print(str3[0:5])

#Выведение всей строки, кроме последних двух символов
str4 = 'Hello world'
print(str4[0:-2])

#Выведение символов с четными индексами
str5 = 'Hello world'
print(str5[1::2])

#Выведение символов с нечетными индексами
str6 = 'Hello world'
print(str6[::2])

#Выведение всех символов в обратном порядке
str7 = 'Hello world'
print(str7[::-1])

#Выведение всех символов через один в обратном порядке
str8 = 'Hello world'
print(str8[::-2])

#Вывод длины строки
str9 = 'Hello world'
print(len(str9))
